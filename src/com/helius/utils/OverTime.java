package com.helius.utils;

import java.sql.Timestamp;

public class OverTime {

	private Timestamp otDate;
	private Timestamp otStarttime;
	private Timestamp otEndtime;
	public Timestamp getOtDate() {
		return otDate;
	}
	public void setOtDate(Timestamp otDate) {
		this.otDate = otDate;
	}
	public Timestamp getOtStarttime() {
		return otStarttime;
	}
	public void setOtStarttime(Timestamp otStarttime) {
		this.otStarttime = otStarttime;
	}
	public Timestamp getOtEndtime() {
		return otEndtime;
	}
	public void setOtEndtime(Timestamp otEndtime) {
		this.otEndtime = otEndtime;
	}
	
	
	
}
